package com.scaler.LLD.TicTacToe;

public class Pair<I,J> {
	int i;
	int j;
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public Pair(int i, int j) {
		super();
		this.i = i;
		this.j = j;
	}
	public int getJ() {
		return j;
	}
	public void setJ(int j) {
		this.j = j;
	}

}
